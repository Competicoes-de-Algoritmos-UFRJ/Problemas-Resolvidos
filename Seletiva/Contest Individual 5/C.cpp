#include "bits/stdc++.h"

using namespace std;

#define fastio ios_base::sync_with_stdio(false), cin.tie(nullptr)
typedef long long ll;

signed solve(){
    ll n,c; cin >> n >> c;

    vector<ll> arr(n);
    for(int i = 0; i < n; i++) cin >> arr[i];

    sort(arr.begin(), arr.end());

    ll l = 1, r = sqrt(c)+5;

    while(l <= r){
        ll sum = 0;
        ll mid = (l+r)/2;

        bool pass = false;

        if((c / (mid*mid)) < n) pass = true;

        for(int i = 0; i < n; i++){
            ll x = arr[i];

            if(sum > c) break;
            if(sum < 0){
                pass = true;
                break;
            }

            sum += (x+2*mid)*(x+2*mid);
        }

        if(sum == c){
            cout << mid << '\n';
            return 0;
        }
        else if(sum > c || pass){
            r = mid-1;
        }
        else l = mid+1;
    }



}

signed main(){
    fastio;

    int t; cin >> t;

    while(t--){
        solve();
    }
    
    return 0;
}
